package com.sytax.class12;

public class GradeMethod {

	char getGrade(int scor) {
		if (scor > 90) {
			return 'A';
		}

		else if (scor > 80) {
			return 'B';
		} else if (scor > 70) {
			return 'C';
		}
		else return 'F';

	}
	String printEmail(String a,String b,String c) {
		
return a+b+c;
	}
	
	
	
	
	
	
	
	
	
	
}
