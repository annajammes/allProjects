package com.sytax.class8;

public class Clock {

	public static void main(String[] args) {

		for (int i = 0; i <= 2; i++) {

			for (int j = 0; j <= 4; j++) {

				for (int k = 0; k <= 5; k++) {

					for (int l = 1; l <= 9; l++) {
						System.out.println(i + " " + j + ": " + k + " " + l);

					}

				}
			}
		}
	}
}
